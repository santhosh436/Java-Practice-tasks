package practice.src;

public class NonMain {

    private int age;
    private int cgpa;

    public NonMain(int age, int cgpa) {
        this.age = age;
        this.cgpa = cgpa;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getCgpa() {
        return cgpa;
    }

    public void setCgpa(int cgpa) {
        this.cgpa = cgpa;
    }
}
